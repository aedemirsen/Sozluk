    private void SozlukAP(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SozlukAP
        // TODO add your handling code here:
        JButton sb = (JButton)evt.getSource();
        d.clear();
        jLabel8.setText("");
        if (sb.equals(degistir)) {
            if (jLabel1.getText().equals("İngilizce")) {
                jLabel1.setText("Türkçe");
            }
            else if (jLabel1.getText().equals("Türkçe")) {
                jLabel1.setText("İngilizce");
            }
            if (jLabel2.getText().equals("İngilizce")) {
                jLabel2.setText("Türkçe");
            }
            else if (jLabel2.getText().equals("Türkçe")) {
                jLabel2.setText("İngilizce");
            }  
            jLayeredPane1.setVisible(true);
            jLayeredPane2.setVisible(false);
        }        
        else if (sb.equals(cevir) && !kelimeGiris.getText().equals("")) { 
            if (jLabel1.getText().equals("Türkçe") ) {
                for (int i = 0; i < turkcedenIngilizceye(kelimeGiris.getText()).size(); i++) {
                    d.addElement(turkcedenIngilizceye(kelimeGiris.getText()).get(i));
                }
                cevap.setModel(d);
                if (exactWordT(kelimeGiris.getText()) == false && !d.isEmpty()) {
                  jDialog2.setVisible(true);
                }
            }
            else if(jLabel1.getText().equals("İngilizce") ){
                for (int i = 0; i < ingilizcedenTurkceye(kelimeGiris.getText()).size(); i++) {
                    d.addElement(ingilizcedenTurkceye(kelimeGiris.getText()).get(i));
                }
                cevap.setModel(d);
                if (exactWordE(kelimeGiris.getText()) == false && !d.isEmpty()) {
                  jDialog2.setVisible(true);
                }
            }
            if (d.isEmpty() && kontrol == 0) {
                jLayeredPane1.setVisible(false);
                jLayeredPane2.setVisible(true);
                kontrol++;
            }
            else if(!d.isEmpty()){
                jLayeredPane1.setVisible(true);
                jLayeredPane2.setVisible(false);
            }
             if (jLabel1.getText().equals("Türkçe") && (s.gecerliMiT(kelimeGiris.getText()) == false)) {
                jDialog1.setVisible(true);
                kontrol++;
            }
            else if (jLabel1.getText().equals("İngilizce") && (s.gecerliMiE(kelimeGiris.getText()) == false)) {
                jDialog1.setVisible(true);
                kontrol++;
            }        
        }
    }//GEN-LAST:event_SozlukAP

