    static ArrayList turkcedenIngilizceye(String kelime/*text field a girilen kelime*/){        
        ArrayList<String> dizi = new ArrayList<>();
        for (int i = 0; i < tur_eng.size(); i++) {
          String t = tur_eng.get(i).substring(0, tur_eng.get(i).indexOf("/"));          
          Pattern p = Pattern.compile("\\b"+kelime+"\\b");
          Matcher m = p.matcher(t);
            if (m.find()) {
                dizi.add(t.concat(": ").concat(tur_eng.get(i).substring(tur_eng.get(i).indexOf("/")+1, tur_eng.get(i).length())));                
            }
        }
       return dizi;  
    }
    static ArrayList ingilizcedenTurkceye(String kelime/*text field a girilen kelime*/){
        ArrayList<String> dizi = new ArrayList<>();
        for (int i = 0; i < eng_tur.size(); i++) {
          String t = eng_tur.get(i).substring(0, eng_tur.get(i).indexOf("/"));
          Pattern p = Pattern.compile("\\b"+kelime+"\\b");
          Matcher m = p.matcher(t);
            if (m.find()) {
                dizi.add(t.concat(": ").concat(eng_tur.get(i).substring(eng_tur.get(i).indexOf("/")+1, eng_tur.get(i).length())));                
            }
        }
       return dizi;  
    }
    static boolean exactWordT(String kelime){
        boolean b = false;
        for (int i = 0; i < tur_eng.size(); i++) {
          String t = tur_eng.get(i).substring(0, tur_eng.get(i).indexOf("/"));
          Pattern p = Pattern.compile(kelime);
          Matcher m = p.matcher(t);
            if (m.matches()) {
                b = true;               
            }
        }
       return b;  
    }
    static boolean exactWordE(String kelime){
        boolean b = false;
        for (int i = 0; i < eng_tur.size(); i++) {
          String t = eng_tur.get(i).substring(0, eng_tur.get(i).indexOf("/"));
          Pattern p = Pattern.compile(kelime);
          Matcher m = p.matcher(t);
            if (m.matches()) {
                b = true;               
            }
        }
       return b;  
    }
