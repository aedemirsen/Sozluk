    private void KelimeEkleAP(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KelimeEkleAP
        // TODO add your handling code here:
        
        JButton kb = (JButton)evt.getSource();
        if (kb.equals(degistir1)) {
            if (jLabel1.getText().equals("İngilizce")) {
                jLabel1.setText("Türkçe");
            }
            else if (jLabel1.getText().equals("Türkçe")) {
                jLabel1.setText("İngilizce");
            }
            if (jLabel2.getText().equals("İngilizce")) {
                jLabel2.setText("Türkçe");
            }
            else if (jLabel2.getText().equals("Türkçe")) {
                jLabel2.setText("İngilizce");
            }
            if (jLabel1.getText().equals("Türkçe")) {
                jLabel3.setText("ingilizce karşılığı");
            }
            else if (jLabel1.getText().equals("İngilizce")) {
                jLabel3.setText("türkçe karşılığı");
            }
        }
        else if (kb.equals(ekle) && (!kelime.getText().equals("") && !karsiligi.getText().equals(""))) { 
            if (jLabel1.getText().equals("Türkçe")) {
              String yeni = "";
              yeni = yeni.concat(kelime.getText()).concat("/").concat(karsiligi.getText());
              T_ekle(yeni);
              Sozluk.jLabel8.setText("EKLENDİ");
            }
            else if (jLabel1.getText().equals("İngilizce")) {
              String yeni = "" ; 
              yeni = yeni.concat(kelime.getText()).concat("/").concat(karsiligi.getText());
              E_ekle(yeni);
              Sozluk.jLabel8.setText("EKLENDİ"); 
            }
            kelime.setText("");
            karsiligi.setText("");
            Sozluk.kontrol = 0;
            this.dispose();
        }
        
    }//GEN-LAST:event_KelimeEkleAP

