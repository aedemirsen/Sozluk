    static void T_ekle(String kelime){
        try {
            BufferedWriter t_ekle = new BufferedWriter(new FileWriter("tureng.txt",true));      
                t_ekle.newLine();
                t_ekle.write(kelime);
                t_ekle.close();
                Sozluk.tur_eng.add(kelime);
        } catch (IOException ex) {
            Logger.getLogger(KelimeEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    static void E_ekle(String kelime){        
        try {
            BufferedWriter e_ekle = new BufferedWriter(new FileWriter("engtur.txt",true));
                e_ekle.newLine();
                e_ekle.write(kelime);
                e_ekle.close(); 
                Sozluk.eng_tur.add(kelime);
        } catch (IOException ex) {
            Logger.getLogger(KelimeEkle.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
